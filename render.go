package main

import (
	"fmt"
	"net/http"
	"text/template"
)

// Render the template
func renderTemplate(w http.ResponseWriter, html string) {
	// Parsing templates
	parsedTemplate, _ := template.ParseFiles("./templates/" + html)
	// Execute that template
	err := parsedTemplate.Execute(w, nil)
	if err != nil {
		fmt.Println("error parsing template:", err)
		return
	}
}
